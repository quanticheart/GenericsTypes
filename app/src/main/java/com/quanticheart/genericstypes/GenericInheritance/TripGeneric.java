package com.quanticheart.genericstypes.GenericInheritance;

public class TripGeneric<T, U, S> {

    private T first;
    private U second;
    private S third;

    public TripGeneric(T first, U second, S third) {
        this.first = first;
        this.second = second;
        this.third = third;
    }

    public T getFirst() {
        return first;
    }

    public void setFirst(T first) {
        this.first = first;
    }

    public U getSecond() {
        return second;
    }

    public void setSecond(U second) {
        this.second = second;
    }

    public S getThird() {
        return third;
    }

    public void setThird(S third) {
        this.third = third;
    }

    @Override
    public String toString() {
        return "TripGeneric{" +
                "first=" + first +
                ", second=" + second +
                ", third=" + third +
                '}';
    }
}
